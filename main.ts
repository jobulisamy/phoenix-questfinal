scene.onHitTile(SpriteKind.Player, 10, function (sprite) {
    if (level == 4 && PhoenixGem == 1) {
        game.over(true)
    } else {
        if (level == 4) {
            game.splash("Find the Gem before it's too late!")
            info.startCountdown(20)
        }
        level += 1
        info.changeLifeBy(1)
        setup_level()
        scene.placeOnRandomTile(Man, 9)
    }
})
